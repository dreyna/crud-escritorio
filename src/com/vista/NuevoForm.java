    private void btnregistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregistrarActionPerformed
        // TODO add your handling code here:
        Post post = new Post();        
        String titulo = txttitulo.getText();
        String desc = txtadescripcion.getText();
        post.setIdpost(idpost);
        post.setTitulo(titulo);
        post.setDescripcion(desc);
        if(idpost==0){
            if(pdao.create(post)==1){
                JOptionPane.showMessageDialog(rootPane, "Registro gradado correctamente..!");
                this.dispose();
                PrincipalForm.limpiar();
                PrincipalForm.listar();

            }else{
                JOptionPane.showMessageDialog(rootPane, "Error al guardar el registro");
            }
        }else{
                    
             if(pdao.update(post)==1){
                JOptionPane.showMessageDialog(rootPane, "Registro modificado correctamente..!");
                this.dispose();
                idpost =0;
                PrincipalForm.limpiar();
                PrincipalForm.listar();

            }else{
                JOptionPane.showMessageDialog(rootPane, "Error al modificar el registro");
            }       
        }
    }//GEN-LAST:event_btnregistrarActionPerformed

    
